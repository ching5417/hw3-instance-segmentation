def binary_mask_to_rle(binary_mask):
    rle = {'counts': [], 'size': list(binary_mask.shape)}
    counts = rle.get('counts')
    for i, (value, elements) in enumerate(groupby(binary_mask.ravel(order='F'))):
        if i == 0 and value == 1:
            counts.append(0)
        counts.append(len(list(elements)))
    compressed_rle = maskUtils.frPyObjects(rle, rle.get('size')[0], rle.get('size')[1])
    compressed_rle['counts'] = str(compressed_rle['counts'], encoding='utf-8')
    return compressed_rle

def evaluate_coco(model, dataset, limit=0):
    """Runs official COCO evaluation.
    dataset: A Dataset object with valiadtion data
    eval_type: "bbox" or "segm" for bounding box or segmentation evaluation
    limit: if not 0, it's the number of images to use for evaluation
    """
    image_dir = os.path.join(ROOT_DIR, "dataset/test_images/")
    PATH = os.path.join(ROOT_DIR, "dataset/test.json")
    coco = COCO(PATH) # load testing annotations
    
    with open(PATH, "r")as file:
        test_json = json.loads(file.read())

    # Pick COCO images from the dataset
    image_ids = []
    for d in test_json['images']:
        image_ids.append(d['id'])

    # Limit to a subset
    if limit:
        image_ids = image_ids[:limit]

    # Get corresponding COCO image IDs.

    results = []
    for image_id in image_ids:
        # Load image
        print("test_images/" + coco.loadImgs(ids=image_id)[0]['file_name'])
        IMAGE_PATH = os.path.join(ROOT_DIR, "dataset/test_images/")
        image = cv2.imread(IMAGE_PATH + coco.loadImgs(ids=image_id)[0]['file_name'])[:, :, ::-1]  # load image
        # Run detection
        r = model.detect([image], verbose=0)[0]
        if r["rois"] is not None:
            for i in range(len(r["scores"])):
                mask = r["masks"].astype(np.uint8)[:, :, i]

                image_results = {}
                image_results['image_id'] = image_id  # this imgid must be same as the key of test.json
                image_results['category_id'] = int(dataset.get_source_class_id(r["class_ids"][i], "coco"))
                image_results['segmentation'] = binary_mask_to_rle(mask)
                image_results['score'] = float(r["scores"][i])
                results.append(image_results)

    OUTPUT_PATH = os.path.join(ROOT_DIR, "output/submission.json")
    with open(OUTPUT_PATH, "w") as f:
        json.dump(results, f)


